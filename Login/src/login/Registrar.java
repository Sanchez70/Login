    public void registrar() throws SQLException {
        usuario nuevo1 = new usuario();
        int auxedad = Integer.parseInt(edad.getText().toString());
        nuevo1.setCedula(cedula.getText());
        nuevo1.setNombre(nombre.getText());
        nuevo1.setApellido(apellido.getText());
        nuevo1.setEdad(auxedad);
        nuevo1.setContra(contra.getText().toString());
        nuevo1.setCorreo(correo.getText());
        nuevo1.setTelefono(telefono.getText());
        nuevo1.setUsuario(usuario.getText());
        nuevo1.registrar();
    }

    public static void success(String actividad, String mensaje) {
        new rojerusan.RSNotifyAnimated(actividad, mensaje,
                5, RSNotifyAnimated.PositionNotify.BottomRight, RSNotifyAnimated.AnimationNotify.RightLeft,
                RSNotifyAnimated.TypeNotify.SUCCESS).setVisible(true);
    }

